/*
    Παράδειγμα 9:
    Να γραφεί ένα πρόγραμμα το οποίο να διαβάζει το βαθμό ενός φοιτητή και, εάν ανήκει στο διαστημα [5, 10], 
    να καταχωρεί την τιμή του σε μία άλλη μεταβλητή με χρήση τον τελεστή "?:". 
    Αλλιώς, να της καταχωρεί την τιμή -1.
*/

#include <stdio.h>

int main(void){
    float grade, i;
    printf("Enter grade: ");
    scanf("%f", &grade);

    i = (grade >= 5 && grade <= 10) ? grade : -1;
    printf("%f\n", i);
    return 0;
}