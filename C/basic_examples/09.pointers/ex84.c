/*
    Παράδειγμα 1:
    Ποια είναι η έξοδος του παρακάτω προγράμματος;
*/


#include <stdio.h>

int main(void)
{
	int *ptr;
	int i = 10;

	ptr = &i;
	i += 20;
	printf("Val = %d\n", *ptr);
	return 0;
}

/*
    Απάντηση:
    Με την εντολή ptr = &i; ο ptr δείχνει στη διεύθυνση της μεταβλητής i, 
    άρα το *ptr θα είναι ίσο με τη τιμή του i. Με την εντολή i += 20; το i γίνεται 30, 
    άρα, αφού το *ptr είναι ίσο με την τιμή του i, το πρόγραμμα θα εμφανίσει: Val = 30.
*/