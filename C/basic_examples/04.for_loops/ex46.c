/*
    Παράδειγμα 12:
    Ποια είναι η έξοδος του παρακάτω προγράμματος
*/

#include <stdio.h>

int main (){
	int i;
	for (i = 3; i && i-1; i--) {
		printf("%d\n", i);
    }
	return 0;
}

/*
    Απάντηση:
    Ο for βρόχος τερματίζεται όταν η συνθήκη i && i-1 γίνει ψευδής. Αυτό θα συμβεί όταν 
    η τιμή του i γίνει ίση με 1. Άρα, ο for βρόχος εκτελείται δυο φορές και το πρόγραμμα εμφανίζει 3 και 2 .
*/