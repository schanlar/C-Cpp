/*
    Παράδειγμα 5: 
    Ποια είναι η έξοδος του παρακάτω προγράμματος;
*/

#include <stdio.h>
int main(void)
{
	int a = 1;
	printf("%d\n", (a > 0) && (--a > 0));
	return 0;
}

/*
    Απάντηση:
    Με την εντολή --a η τιμή της μεταβλητής a γίνεται 0. Αρα, αφού ο όρος (--a > 0) είναι ψευδής, 
    το πρόγραµμα εμφανίζει 0.
*/