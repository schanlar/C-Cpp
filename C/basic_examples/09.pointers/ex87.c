/*
    Παράδειγμα 4:
    Ποια είναι η έξοδος του παρακάτω προγράμματος;
*/
#include <stdio.h>

int main()
{
	int *ptr, i=0;
	
	for(ptr = &i; i < 3; i++)
		printf("%d", *ptr);
	return 0;
}

/*
    Απάντηση:
    Με την εντολή ptr = &i; ο ptr δείχνει στη διεύθυνση της μεταβλητής i, 
    άρα το *ptr θα είναι ίσο με την τιμή του i.

    Στον for βρόχο η τιμή του i γίνεται διαδοχικά 0, 1, 2, 
    άρα το πρόγραμμα θα εμφανίσει: 0 1 2
*/