/*
    Παράδειγμα 2:
    Ποια είναι η έξοδος του παρακάτω προγράμματος;
*/

#include <stdio.h>
int main()
{
    int a = -14;
    printf("Num = %d\n", !!a);
    return 0;
}

/*
    Απάντηση: 
    Αφού η τιμή του α είναι -14, η τιμή της έκφρασης !α είναι 0. Άρα η έκφραση !!α είναι ισοδύναμη με !0 
    και το πρόγραμμα εμφανίζει: Num = 1
*/